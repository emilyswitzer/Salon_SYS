-- Build Tables
CREATE TABLE Employees
(EmployeeID numeric(8) NOT NULL,
 EmpName varchar2 (15) NOT NULL,
 EmpSurname varchar2 (20) NOT NULL,
 Email varchar2 (30) NOT NULL,
 EmpStatus char(1),
 CONSTRAINT pk_Employees PRIMARY KEY (EmployeeID));

CREATE TABLE TimeSlots
 (DurationID numeric(5)NOT NULL,
 Duration varchar2(5) NOT NULL,
 CONSTRAINT pk_TimeSlots PRIMARY KEY(DurationID));
 
 CREATE Table AppTimes
 (AppTimeID numeric(5)NOT NULL,
 AppTime varchar2(5) NOT NULL,
 CONSTRAINT pk_AppTimes PRIMARY KEY(AppTimeID));


CREATE TABLE Services
(ServiceID numeric(8)NOT NULL,
 ServDescription varchar2(100) NOT NULL,
 DurationID numeric(5) NOT NULL,
 Cost decimal(5,2) NOT NULL,
 ServStatus char(1) NOT NULL,
 CONSTRAINT pk_Services PRIMARY KEY (ServiceID),
 CONSTRAINT fk_Services FOREIGN KEY (DurationID) REFERENCES TimeSlots);


CREATE TABLE EmployeeServices
(ServiceID numeric (8),
 EmployeeID numeric (8),
 CONSTRAINT pk_EmloyeeServices PRIMARY KEY (ServiceID, EmployeeID),
 CONSTRAINT fk_EmpServ_Services FOREIGN KEY (ServiceID) REFERENCES Services,
 CONSTRAINT fk_EmpServ_Employees FOREIGN KEY (EmployeeID) REFERENCES Employees);
 
 CREATE TABLE Appointments
 (AppID numeric(8)NOT NULL,
  AppDate Date NOT NULL,
  AppTimeID numeric(5) NOT NULL,
  EndTime varchar2(5) NOT NULL,
  ServiceID numeric(8) NOT NULL,
  EmployeeID numeric(8) NOT NULL,
  AppCost decimal(5,2) NOT NULL,
  Client varchar2(15) NOT NULL,
  Phone numeric(10) NOT NULL,
  CONSTRAINT pk_Appointments PRIMARY KEY (AppID),
  CONSTRAINT fk_Appointments_AppTimes_ FOREIGN KEY (AppTimeID) REFERENCES AppTimes,
  CONSTRAINT fk_Appointments_Emps_ FOREIGN KEY (EmployeeID) REFERENCES Employees,
  CONSTRAINT fk_Appointments_Servs_ FOREIGN KEY (ServiceID) REFERENCES Services);
  
CREATE TABLE All_Months
(Month numeric(2) NOT NULL,
CONSTRAINT pk_Months PRIMARY KEY (Month));
COMMIT;

